import java.util.Scanner;
class MatrixPatern 
{
	public void matrix()
	{
		Scanner s=new Scanner(System.in);
		System.out.print("enter the size of matrix:");
		int n=s.nextInt();
		int [][]a=new int[n][n];
		System.out.print("enter the matrix element:");
		for (int i=0;i<n ;i++ )
		{
			for (int j=0;j<n ;j++ )
			{
				a[i][j]=s.nextInt();
			}
		}
		System.out.println("                   Matrix                             ");
		System.out.println("======================================================");
		for (int i=0;i<n ;i++ )
		{
			for (int j=0;j<n ;j++ )
			{
				System.out.print( a[i][j]+ "    ");
			}
			System.out.println();
		}
	}
	public static void main(String[] args) 
	{
		MatrixPatern obj=new MatrixPatern();
		obj.matrix();
	}
}
