
public  abstract class VariableInAbstructClass {
	int a=40;
	static int b=455;
	public int c=50;
	public static int e=50;
	protected int h=232;
	protected static int i=5959;
	private int t=32;
	private static int w=11000;
	public void add(VariableInAbstructClass obj)
	{
		System.out.println(obj.a);
		System.out.println(b);
		System.out.println(obj.c);
		System.out.println(e);
		System.out.println(obj.h);
		System.out.println(i);
		System.out.println(obj.t);
		System.out.println(w);
	}
	

}
